//using for loop to print number from 1-10
console.log("The Numbers from 1-10 using for loop:");
for (let i = 1; i <= 10; i++) 
{
  console.log("Current Number is:"+i);
}


//using for loop to print number from 10-1
console.log("The number from 10-1 using for loop");
for(let i=10; i>=1; i--)
{
    console.log(i);
}